package com.revature.q11_1;

public class FloatContainer {
	/*
	 * The instance variables must be public in order for us to
	 * access them directly from another package
	 * Another way to do this would be to make them protected
	 * and have the other class subclass FloatContainer
	 * We could then create an instance of FloatContainerUser
	 * and access the instance variables of FloatContainer directly
	 * because the protected access modified allows this
	 */
	public float float1 = 23123.232f;
	public float float2 = 3.14f;
}
