package com.revature.pojos;

public enum Status {
	PENDING(1, "pending"), APPROVED(2, "approved"), DENIED(3, "denied");
	
	private int id;
	private String str;
	
	private Status(int id, String str) {
		this.id = id;
		this.str = str;
	}
	
	
	public int getId() {
		return id;
	}
	
	public String getStr() {
		return str;
	}
}
