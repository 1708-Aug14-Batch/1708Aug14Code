CREATE OR REPLACE FUNCTION FINDPERSON(FN IN VARCHAR2)
RETURN NUMBER
AS PERSON_ID NUMBER;

CURSOR C1 IS
SELECT EX_ID
FROM EXAMPLE
WHERE LOWER(F_NAME) = LOWER(FN);

BEGIN

OPEN C1;
FETCH C1 INTO PERSON_ID;

IF C1%NOTFOUND THEN
PERSON_ID := -1;
END IF;
CLOSE C1;
RETURN PERSON_ID;
END;

