package Q16;

public class NumOfChars {
	// Write a program to display the number of characters for a string input. 
	// The string should be entered as a command line argument using (String [ ] args).
	
	public static void main(String[] args) {
		System.out.println("The input string is: " + args[0]);
		System.out.println("The number of characters in the String is: " + args[0].length());
	}

}
